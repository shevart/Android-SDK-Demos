package com.shevart.androidcorelearn.multi_threading.test_task_1;

@SuppressWarnings("WeakerAccess")
public class Interval {
    private int id;
    private int low;
    private int high;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLow() {
        return low;
    }

    public void setLow(int low) {
        this.low = low;
    }

    public int getHigh() {
        return high;
    }

    public void setHigh(int high) {
        this.high = high;
    }
}
